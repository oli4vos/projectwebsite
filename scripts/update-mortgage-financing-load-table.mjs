import fs from "node:fs/promises";
import path from "node:path";
import process from "node:process";

const DEFAULT_YEAR = 2026;
const DEFAULT_SOURCE_URL =
  "https://zoek.officielebekendmakingen.nl/stcrt-2025-36471.html";
const DEFAULT_OUTPUT =
  "src/lib/financial-constants/mortgage-financing-load-data.ts";

function readArgument(name, fallback) {
  const prefix = `--${name}=`;
  const value = process.argv.find((argument) => argument.startsWith(prefix));
  return value ? value.slice(prefix.length) : fallback;
}

function decodeHtml(value) {
  return value
    .replace(/<[^>]+>/g, " ")
    .replace(/&nbsp;|&#160;| /g, " ")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&amp;/g, "&")
    .replace(/\s+/g, " ")
    .trim();
}

function parsePercentage(value) {
  const parsed = Number(value.replace("%", "").replace(",", "."));
  if (!Number.isFinite(parsed)) {
    throw new Error(`Ongeldig financieringslastpercentage: ${value}`);
  }
  return parsed;
}

function parseIncome(value) {
  if (value === "–" || value === "-") {
    return 0;
  }

  const parsed = Number(value.replace(/[^0-9]/g, ""));
  if (!Number.isFinite(parsed)) {
    throw new Error(`Ongeldige inkomensgrens: ${value}`);
  }
  return parsed;
}

function parseRateBand(label) {
  if (label.includes(">=")) {
    return { maxRate: null, label };
  }

  const matches = [...label.matchAll(/\d+[,.]\d+/g)];
  const upperBound = matches.at(-1)?.[0];
  if (!upperBound) {
    throw new Error(`Ongeldige rentekolom: ${label}`);
  }

  return {
    maxRate: Number(upperBound.replace(",", ".")),
    label,
  };
}

function parseFinancingTable(tableHtml) {
  const header = tableHtml.match(/<thead\b[\s\S]*?<\/thead>/)?.[0];
  const body = tableHtml.match(/<tbody\b[\s\S]*?<\/tbody>/)?.[0];
  if (!header || !body) {
    throw new Error("Financieringslasttabel mist een header of body.");
  }

  const headerCells = [...header.matchAll(/<th\b[\s\S]*?<\/th>/g)].map(
    (match) => decodeHtml(match[0]),
  );
  const rateBands = headerCells
    .slice(1, 13)
    .map((label) => parseRateBand(label));

  const rows = [...body.matchAll(/<tr\b[\s\S]*?<\/tr>/g)].map(
    (rowMatch) => {
      const cells = [...rowMatch[0].matchAll(/<td\b[\s\S]*?<\/td>/g)].map(
        (cellMatch) => decodeHtml(cellMatch[0]),
      );

      if (cells.length !== 13) {
        throw new Error(
          `Onverwacht aantal kolommen: ${cells.length}; verwacht 13.`,
        );
      }

      return {
        minIncome: parseIncome(cells[0]),
        percentages: cells.slice(1).map(parsePercentage),
      };
    },
  );

  return { rateBands, rows };
}

function formatRows(rows) {
  return rows
    .map(
      (row) =>
        `    { minIncome: ${row.minIncome}, percentages: [${row.percentages.join(", ")}] },`,
    )
    .join("\n");
}

function formatRateBands(rateBands) {
  return rateBands
    .map(
      (band) =>
        `    { maxRate: ${band.maxRate ?? "null"}, label: ${JSON.stringify(band.label)} },`,
    )
    .join("\n");
}

function buildOutput({ year, sourceUrl, rateBands, beforeAow, fromAow }) {
  return `// Generated by scripts/update-mortgage-financing-load-table.mjs. Do not edit manually.\n` +
    `import type { MortgageFinancingLoadData } from "@/lib/financial-constants/types";\n\n` +
    `export const MORTGAGE_FINANCING_LOAD_DATA = {\n` +
    `  normYear: ${year},\n` +
    `  versionLabel: "Wijzigingsregeling hypothecair krediet ${year}",\n` +
    `  sourceLabel: "Staatscourant financieringslastpercentages ${year}",\n` +
    `  sourceUrl: ${JSON.stringify(sourceUrl)},\n` +
    `  lastChecked: ${JSON.stringify(new Date().toISOString().slice(0, 10))},\n` +
    `  status: "definitief",\n` +
    `  rateBands: [\n${formatRateBands(rateBands)}\n  ],\n` +
    `  beforeAow: [\n${formatRows(beforeAow)}\n  ],\n` +
    `  fromAow: [\n${formatRows(fromAow)}\n  ],\n` +
    `} as const satisfies MortgageFinancingLoadData;\n`;
}

async function main() {
  const year = Number(readArgument("year", String(DEFAULT_YEAR)));
  const sourceUrl = readArgument("source", DEFAULT_SOURCE_URL);
  const outputPath = path.resolve(
    process.cwd(),
    readArgument("output", DEFAULT_OUTPUT),
  );

  if (!Number.isInteger(year) || year < 2000) {
    throw new Error(`Ongeldig normjaar: ${year}`);
  }

  const response = await fetch(sourceUrl);
  if (!response.ok) {
    throw new Error(`Bron ophalen mislukt: HTTP ${response.status}`);
  }

  const html = await response.text();
  const financingTables = [...html.matchAll(/<table\b[\s\S]*?<\/table>/g)]
    .map((match) => match[0])
    .filter((table) => table.includes("toetsrente") && table.includes("toets-"));

  if (financingTables.length < 2) {
    throw new Error(
      `Verwacht minimaal twee financieringslasttabellen, gevonden: ${financingTables.length}.`,
    );
  }

  const beforeAow = parseFinancingTable(financingTables[0]);
  const fromAow = parseFinancingTable(financingTables[1]);

  if (
    JSON.stringify(beforeAow.rateBands) !== JSON.stringify(fromAow.rateBands)
  ) {
    throw new Error("Rentekolommen verschillen tussen de twee AOW-tabellen.");
  }

  const output = buildOutput({
    year,
    sourceUrl,
    rateBands: beforeAow.rateBands,
    beforeAow: beforeAow.rows,
    fromAow: fromAow.rows,
  });

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, output, "utf8");
  console.log(
    `Officiële financieringslasttabel ${year} bijgewerkt: ${path.relative(process.cwd(), outputPath)}`,
  );
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
